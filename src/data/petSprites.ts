const PET_SPRITE_OPTION_MAP = PET_SPRITE_OPTIONS.reduce<Record<string, PetSpriteOption>>(
  (result, option) => {
    result[option.key] = option;
    return result;
  },
  {},
);

export function getPetSpriteConfigByKey(key: string, action: PetSpriteAction) {
  return PET_SPRITES[key]?.[action] ?? null;
}

export function hasPetSpriteAction(key: string, action: PetSpriteAction) {
  return Boolean(PET_SPRITES[key]?.[action]);
}

export function getPetSpriteOptionByKey(key: string) {
  return PET_SPRITE_OPTION_MAP[key] ?? null;
}

export function isPetSpriteKey(key: string | null | undefined) {
  if (!key) return false;
  return Boolean(PET_SPRITE_OPTION_MAP[key]);
}
